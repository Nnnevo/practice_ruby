# 2. Порахувати, скільки грошей буде заощаджено. Дано три цілих: ціна товару, знижка (у відсотках) за одиницю товару, кількість товару. 
#Повернути: заощаджену суму (ціле число, заокруглити, якщо потрібно).

puts ("Введіть ціну: ")
price = gets.chomp()

puts ("Введіть знижку: ")
discount = gets.chomp()

puts ("Введіть к-сть товару: ")
number = gets.chomp()

saved = (price.to_f * discount.to_f / 100) * number.to_i

puts ("Заощаджено: " + saved.round().to_s)
sleep 3